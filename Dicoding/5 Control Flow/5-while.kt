//Contoh While
fun main() {
    var counter = 1
    while (counter <= 7){
        println("Hello, World!")
        counter++
    }
}

/*
counter awalnya bernilai 1, kemudian pada perulangan while, nilai counter terus bertambah 1 
sehingga mencetak "Hello, World!" sebanyak kurang dari sama dengan 7
*/
